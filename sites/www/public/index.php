document.addEventListener('DOMContentLoaded', () => {
  const sliderContainer = document.getElementById('slider-container');
  const slider = document.getElementById('slider');
  const slides = document.querySelectorAll('.slide');
  const totalSlides = slides.length;
  const bgVideo1 = document.getElementById('bg-video-1');
  const bgVideo2 = document.getElementById('bg-video-2');

  const videoSources = [
    "https://glimmio.com/image/Main-Section/PM Video.webm",
    "https://glimmio.com/image/Main-Section/WD Video.webm",
    "https://glimmio.com/image/Main-Section/SEO Video.webm",
    "https://glimmio.com/image/Main-Section/IM Video.webm",
    "https://glimmio.com/image/Main-Section/SMM Video.webm"
  ];

  let currentSlide = 0;
  let autoSlideInterval;
  let autoSlideStopped = false;
  let startX = 0;
  let activeBg = 1;

  function stopAutoSlide() {
    if (!autoSlideStopped) {
      clearInterval(autoSlideInterval);
      autoSlideStopped = true;
    }
  }

  function updateSlider() {
    // Update image slide position
    slider.style.transform = `translateX(-${currentSlide * 100}vw)`;

    // Background video swap logic
    const newSrc = videoSources[currentSlide];
    const currentVideo = activeBg === 1 ? bgVideo1 : bgVideo2;
    const nextVideo = activeBg === 1 ? bgVideo2 : bgVideo1;

    // Avoid unnecessary reload if video already active
    if (nextVideo.querySelector('source').src.includes(newSrc)) return;

    // Reset next video and load new source
    nextVideo.pause();
    nextVideo.querySelector('source').src = newSrc;
    nextVideo.load();

    // Remove any previous listener
    nextVideo.oncanplaythrough = null;

    // When the new video is ready to play, fade it in
    nextVideo.oncanplaythrough = () => {
      nextVideo.classList.add('active');
      currentVideo.classList.remove('active');
      nextVideo.play();
      activeBg = activeBg === 1 ? 2 : 1;
    };
  }

  function moveSlide(direction) {
    currentSlide = (currentSlide + direction + totalSlides) % totalSlides;
    updateSlider(); // Immediately update background video
  }

  // Navigation Buttons
  document.getElementById('prevBtn')?.addEventListener('click', () => {
    moveSlide(-1);
    stopAutoSlide(); // Stop auto-scroll on manual click
  });

  document.getElementById('nextBtn')?.addEventListener('click', () => {
    moveSlide(1);
    stopAutoSlide(); // Stop auto-scroll on manual click
  });

  // Touch Support for Swipe
  slider.addEventListener('touchstart', e => {
    startX = e.touches[0].clientX;
  });

  slider.addEventListener('touchend', e => {
    const endX = e.changedTouches[0].clientX;
    const threshold = 50;

    if (startX - endX > threshold) {
      moveSlide(1);
      stopAutoSlide();
    } else if (endX - startX > threshold) {
      moveSlide(-1);
      stopAutoSlide();
    }
  });

  // Auto Slide Every 5 Seconds
  autoSlideInterval = setInterval(() => {
    currentSlide = (currentSlide + 1) % totalSlides;
    updateSlider();
  }, 5000);

  // Show slider on scroll
  window.addEventListener('scroll', () => {
    if (window.scrollY > 50) {
      sliderContainer.classList.add('slider-visible');
    } else {
      sliderContainer.classList.remove('slider-visible');
    }
  });
});



