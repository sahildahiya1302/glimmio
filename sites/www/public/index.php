document.addEventListener('DOMContentLoaded', () => {
        const logoItems = document.querySelectorAll('.logo-item');
        const caseStudyContents = document.querySelectorAll('.case-study-content');
        const logoCarousel = document.querySelector('.logo-carousel');
        
        // Show the first case study by default
        caseStudyContents[0].classList.add('active');
        logoItems[0].classList.add('active');
      
        let currentIndex = 0; // Track the current index
        const totalLogos = logoItems.length; // Total number of logos
        const autoSlideInterval = 4000; // Time in milliseconds (5 seconds)
      
        // Function to show the next logo and case study
        const showNextLogo = () => {
          // Remove active class from current logo and case study
          logoItems[currentIndex].classList.remove('active');
          caseStudyContents[currentIndex].classList.remove('active');
      
          // Update current index to show the next logo
          currentIndex = (currentIndex + 1) % totalLogos; // Loop back to start
      
          // Add active class to the next logo and case study
          logoItems[currentIndex].classList.add('active');
          caseStudyContents[currentIndex].classList.add('active');
      
          // Scroll smoothly to bring the selected logo to the middle
          const item = logoItems[currentIndex]; // Get the current item
          const itemOffsetTop = item.offsetTop;
          const itemHeight = item.clientHeight;
          const containerHeight = logoCarousel.clientHeight;
      
          const scrollPosition = itemOffsetTop - (containerHeight / 2 - itemHeight / 2);
          logoCarousel.scrollTo({
            top: scrollPosition,
            behavior: 'smooth'
          });
        };
      
        // Automatically change logos every 5 seconds
        const intervalId = setInterval(showNextLogo, autoSlideInterval);
      
        logoItems.forEach((item, index) => {
          item.addEventListener('click', () => {
            // Clear the automatic sliding interval
            clearInterval(intervalId);
      
            // Remove active class from all case studies and logos
            caseStudyContents.forEach(content => content.classList.remove('active'));
            logoItems.forEach(logo => logo.classList.remove('active'));
      
            // Add active class to the selected case study and logo
            const targetId = item.getAttribute('data-target');
            document.getElementById(targetId).classList.add('active');
            item.classList.add('active');
      
            // Scroll smoothly to bring the selected logo to the middle
            const itemOffsetTop = item.offsetTop;
            const itemHeight = item.clientHeight;
            const containerHeight = logoCarousel.clientHeight;
      
            const scrollPosition = itemOffsetTop - (containerHeight / 2 - itemHeight / 2);
            logoCarousel.scrollTo({
              top: scrollPosition,
              behavior: 'smooth'
            });
          });
        });
      });
      


